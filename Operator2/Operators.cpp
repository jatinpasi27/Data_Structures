#include <iostream>
using namespace std;
int main()
{
    int a = 4;
    int b = 5;
    cout << "+" << (a + b) << endl; // 9
    cout << "-" << (a - b) << endl; //-1
    cout << "*" << (a * b) << endl; //20 
    cout << "/" << (a / b) << endl; //0
    cout << "%" << (a % b) << endl; //4
    return 0;
}